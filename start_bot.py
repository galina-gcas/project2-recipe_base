import os
import time
import requests
from config import BOT_TOKEN

def start_bot_automatically():
    """–ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –∑–∞–ø—É—Å–∫–∞–µ—Ç –±–æ—Ç–∞ —á–µ—Ä–µ–∑ API"""
    if not BOT_TOKEN:
        print("‚ùå BOT_TOKEN –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω!")
        return False
    
    # URL –≤–∞—à–µ–≥–æ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è (–∑–∞–º–µ–Ω–∏—Ç–µ –Ω–∞ –≤–∞—à)
    app_url = "https://yourusername.pythonanywhere.com"  # –ó–∞–º–µ–Ω–∏—Ç–µ yourusername
    
    try:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—Ç–∞—Ç—É—Å
        response = requests.get(f"{app_url}/status", timeout=10)
        if response.status_code == 200:
            data = response.json()
            if data.get('bot_running'):
                print("‚úÖ –ë–æ—Ç —É–∂–µ –∑–∞–ø—É—â–µ–Ω")
                return True
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞
        print("üöÄ –ó–∞–ø—É—Å–∫–∞–µ–º –±–æ—Ç–∞...")
        response = requests.post(f"{app_url}/start", timeout=10)
        
        if response.status_code == 200:
            data = response.json()
            print(f"‚úÖ {data.get('message', '–ë–æ—Ç –∑–∞–ø—É—â–µ–Ω')}")
            return True
        else:
            print(f"‚ùå –û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞: {response.text}")
            return False
            
    except requests.exceptions.RequestException as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏—è: {e}")
        return False

